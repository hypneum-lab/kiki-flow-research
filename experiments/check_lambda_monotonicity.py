"""Numerical test for lambda-monotonicity of the T2 multi-species Wasserstein flow.

Empirically estimates the contraction rate lambda_hat for the flow generated by
:class:`kiki_flow_core.track2_paper.paper_f.T2FreeEnergy` on the 4-species
Levelt-Baddeley coupling, following the multi-species Wasserstein-flow
contraction framework of Conger, Hoffmann, Mazumdar & Ratliff (2025)
"Nash Equilibria in Multi-Population Mean Field Games and Multi-Agent
Wasserstein Gradient Flows" (arXiv:2506.22947).

A flow is lambda-monotone at rate lambda >= 0 if, for any two trajectories
(rho_i(t))_i and (rho'_i(t))_i starting from different initial conditions,

    d/dt  sum_i W_2^2(rho_i(t), rho'_i(t))  <=  -2 lambda * sum_i W_2^2(...)

When lambda > 0 this implies exponential contraction to the unique Nash
equilibrium at rate 2 lambda (hence the log-linear regression slope in the
estimator below).

Usage
-----
    uv run python experiments/check_lambda_monotonicity.py \\
        --steps 50 --n-pairs 3 --seed-base 42
"""

from __future__ import annotations

import argparse
import json
import sys
from dataclasses import dataclass
from pathlib import Path
from typing import Any

