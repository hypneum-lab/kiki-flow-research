import numpy as np  # noqa: E402
import ot  # noqa: E402

from kiki_flow_core.master_equation import JKOStep  # noqa: E402
from kiki_flow_core.species import CanonicalSpecies  # noqa: E402
from kiki_flow_core.state import FlowState  # noqa: E402
from kiki_flow_core.track2_paper.paper_f import T2FreeEnergy  # noqa: E402
from kiki_flow_core.wasserstein_ops import sinkhorn_cost, w2_distance  # noqa: E402

# --- classification thresholds (named so they are not magic numbers) --------
_MONOTONE_STD_FRACTION = 0.30  # std(lambda_hat) < 30% of |mean| -> consistent
_FIT_MIN_FRACTION = 0.10  # fit regression on d(t) >= 10% of d(0)
_FIT_MIN_POINTS = 4  # need >= 4 valid (t, log d) pairs to fit
_GROWTH_TOL = 1e-9  # ignore d(t) <= GROWTH_TOL as numerical noise
_OSCILLATION_RATIO = 1.05  # d(t) > 1.05 * d(t-1) counts as a rebound
_MEAN_NEAR_ZERO = 1e-6  # |mean lambda_hat| below this treated as numerically zero


@dataclass
class PairResult:
    """Per-pair lambda_hat estimate and the raw W2^2-sum trajectory."""

    seed_a: int
    seed_b: int
    trajectory: list[float]
    lambda_hat: float
    fit_points: int
    monotone: bool
    rebounded: bool


def _dirichlet_density(
    rng: np.random.Generator, n_bins: int, concentration: float = 1.0
) -> np.ndarray:
    """Return a single Dirichlet sample on an ``n_bins``-simplex as float64."""
    alpha = np.full(n_bins, concentration, dtype=np.float64)
    rho = rng.dirichlet(alpha).astype(np.float64)
    # Dirichlet sum is 1 up to fp rounding; clip for numerical safety and re-normalize.
    rho = np.clip(rho, 1e-12, None)
    return rho / rho.sum()


def _make_flow_state(
    rng: np.random.Generator,
    species_names: list[str],
    n_bins: int,
    concentration: float,
) -> FlowState:
    """Build a valid T2 FlowState with independent Dirichlet densities per species."""
    rho = {n: _dirichlet_density(rng, n_bins, concentration) for n in species_names}
    return FlowState(
        rho=rho,
        P_theta=np.zeros(4, dtype=np.float64),
        mu_curr=np.full(n_bins, 1.0 / n_bins, dtype=np.float64),
        tau=0,
        metadata={"track_id": "T2"},
    )


def _sinkhorn_divergence_sq(
    a: np.ndarray,
    b: np.ndarray,
    cost_matrix: np.ndarray,
    epsilon: float,
    n_iter: int,
) -> float:
    """Debiased Sinkhorn divergence S_eps(a, b) = OT(a, b) - 0.5*OT(a, a) - 0.5*OT(b, b).

    This is used in place of the raw entropic-Sinkhorn W2^2 because the latter has
    a non-zero entropic bias even when ``a == b`` (it quickly dominates the signal
    as trajectories approach equilibrium). The debiased divergence is non-negative
    and equals 0 iff ``a == b`` in the epsilon -> 0 limit.
    """
    ab = sinkhorn_cost(a, b, cost_matrix, epsilon=epsilon, n_iter=n_iter)
    aa = sinkhorn_cost(a, a, cost_matrix, epsilon=epsilon, n_iter=n_iter)
    bb = sinkhorn_cost(b, b, cost_matrix, epsilon=epsilon, n_iter=n_iter)
    return max(0.0, ab - 0.5 * (aa + bb))


def _sum_w2_squared(
    state_a: FlowState,
    state_b: FlowState,
    support: np.ndarray,
    cost_matrix: np.ndarray | None = None,
    epsilon: float = 0.01,
    n_iter: int = 500,
    debiased: bool = True,
) -> float:
    """Return sum_i W2^2(rho_i^a, rho_i^b), summed over all species.

    When ``debiased=True`` (default) uses the Sinkhorn divergence to remove the
    entropic bias so that identical distributions give 0 instead of the epsilon-dependent
    floor. Otherwise falls back to the raw biased Sinkhorn cost (kept for reference).
    """
    if cost_matrix is None:
        cost_matrix = ot.dist(support, support, metric="sqeuclidean")
    total = 0.0
    for name, rho_a in state_a.rho.items():
        rho_b = state_b.rho[name]
        if debiased:
            total += _sinkhorn_divergence_sq(
                rho_a, rho_b, cost_matrix, epsilon=epsilon, n_iter=n_iter
            )
        else:
            w2 = w2_distance(rho_a, rho_b, support=support)
            total += float(w2 * w2)
    return total


def _fit_lambda(
    trajectory: list[float],
    h: float = 1.0,
) -> tuple[float, int]:
    """Fit log d(t) ~= log d(0) - 2 lambda t on the initial decaying portion.

    ``h`` is the physical time per sample (= JKO step ``h * n_inner``). When
    ``h==1`` lambda is reported per-step; when ``h`` is the actual step size
    lambda is reported per unit of physical time.

    Returns ``(lambda_hat, n_points)``. ``lambda_hat`` is ``nan`` if too few
    valid points survive the positivity / threshold mask.
    """
    traj = np.asarray(trajectory, dtype=np.float64)
    if traj.size < _FIT_MIN_POINTS or traj[0] <= _GROWTH_TOL:
        return float("nan"), 0

    d0 = traj[0]
    threshold = max(_FIT_MIN_FRACTION * d0, _GROWTH_TOL)
    mask = traj >= threshold
    if mask.sum() < _FIT_MIN_POINTS:
        return float("nan"), int(mask.sum())

    t = np.arange(traj.size, dtype=np.float64)[mask] * h
    y = np.log(traj[mask])
    # np.polyfit returns [slope, intercept]; slope == -2 * lambda_hat
    slope, _intercept = np.polyfit(t, y, 1)
    lambda_hat = -0.5 * float(slope)
    return lambda_hat, int(mask.sum())


def _trajectory_diagnostics(trajectory: list[float]) -> tuple[bool, bool]:
    """Return ``(monotone_decreasing, rebounded)`` flags for classification."""
    traj = np.asarray(trajectory, dtype=np.float64)
    diffs = np.diff(traj)
    monotone = bool(np.all(diffs <= 0.0))
    # rebound = any step where d increases by more than OSCILLATION_RATIO
    # (guards against pure numerical noise at machine precision).
    rebounded = False
    for k in range(1, traj.size):
        prev = max(traj[k - 1], _GROWTH_TOL)
        if traj[k] > _OSCILLATION_RATIO * prev:
            rebounded = True
            break
    return monotone, rebounded


def _classify(per_pair: list[PairResult]) -> tuple[str, float, float]:
    """Classify the flow from per-pair lambda_hats and trajectory shapes.

    Returns ``(label, lambda_hat_mean, lambda_hat_std)``.
    """
    valid = [p.lambda_hat for p in per_pair if np.isfinite(p.lambda_hat)]
    if not valid:
        return "non-monotone (no fit)", float("nan"), float("nan")

    mean = float(np.mean(valid))
    std = float(np.std(valid))
    all_monotone = all(p.monotone for p in per_pair)
    any_rebound = any(p.rebounded for p in per_pair)

    if any_rebound:
        return "non-monotone", mean, std

    # "consistent" requires std < 30% of |mean|, but only once |mean| is
    # numerically distinguishable from zero (otherwise std/|mean| blows up).
    consistent = (abs(mean) < _MEAN_NEAR_ZERO) or (std < _MONOTONE_STD_FRACTION * abs(mean))

    if all_monotone and consistent and mean > 0.0:
        return f"lambda-monotone with lambda_hat ~= {mean:.4f}", mean, std
    if all_monotone and mean <= 0.0:
        return "non-monotone (no net contraction)", mean, std
    return "locally monotone with basin issues", mean, std


def _try_make_plot(
    per_pair: list[PairResult],
    out_path: Path,
) -> tuple[bool, str | None]:
    """Plot log-scale W2^2-sum trajectories. Returns ``(made, note)``.

    matplotlib is imported locally so the script still runs in environments
    without it (the deliverable explicitly allows skipping the plot).
    """
    try:
        import matplotlib  # noqa: PLC0415 - optional dependency, must stay local

        matplotlib.use("Agg")
        import matplotlib.pyplot as plt  # noqa: PLC0415
    except ImportError as exc:  # pragma: no cover - tested via skip path
        return False, f"matplotlib unavailable: {exc}"

    fig, ax = plt.subplots(figsize=(7, 4.5))
    for p in per_pair:
        traj = np.asarray(p.trajectory, dtype=np.float64)
        # mask non-positive for log scale; keep legend even if masked
        positive = traj > _GROWTH_TOL
        ax.plot(
            np.arange(traj.size)[positive],
            traj[positive],
            marker="o",
            markersize=3,
            linewidth=1.0,
            label=f"pair ({p.seed_a}, {p.seed_b}): lambda_hat={p.lambda_hat:.4f}",
        )
    ax.set_yscale("log")
    ax.set_xlabel("JKO step t")
    ax.set_ylabel(r"$\sum_i W_2^2(\rho_i(t), \rho'_i(t))$")
    ax.set_title("lambda-monotonicity test: coupled-pair W2^2 trajectories")
    ax.grid(True, which="both", linestyle=":", alpha=0.5)
    ax.legend(fontsize=7, loc="best")
    fig.tight_layout()
    out_path.parent.mkdir(parents=True, exist_ok=True)
    fig.savefig(out_path, dpi=120)
    plt.close(fig)
    return True, None


def run(
    steps: int,
    n_pairs: int,
    seed_base: int,
    output_json: Path,
    coupling_variant: str | None,
    grid_size: int,
    h: float,
    dirichlet_concentration: float,
    n_inner: int = 1,
    sinkhorn_epsilon: float = 0.01,
    sinkhorn_iters: int = 500,
) -> dict[str, Any]:
    """Estimate lambda_hat over ``n_pairs`` coupled trajectories and classify."""
    species_kwargs: dict[str, Any] = {}
    if coupling_variant is not None:
        species_kwargs["coupling_variant"] = coupling_variant
    species = CanonicalSpecies(**species_kwargs)
    names = species.species_names()

    support = np.linspace(-2.0, 2.0, grid_size).reshape(-1, 1)
    potentials = {n: np.zeros(grid_size, dtype=np.float64) for n in names}
    prior = {n: np.full(grid_size, 1.0 / grid_size, dtype=np.float64) for n in names}
    f_func = T2FreeEnergy(
        species=species,
        potentials=potentials,
        prior=prior,
        turing_strength=0.1,
    )
    # Plain JKO (projected-gradient only, no W2 prox) — cheap, same fixed points
    # under T2FreeEnergy and sufficient for observing contraction. ``n_inner=1``
    # by default so each sampled ``t`` actually corresponds to one discrete-time
    # flow step (coarser n_inner collapses many substeps into one sample and
    # hides the contraction rate above the entropic-bias floor).
    jko = JKOStep(
        f_functional=f_func,
        h=h,
        support=support,
        n_inner=n_inner,
        apply_w2_prox=False,
    )

    cost_matrix = ot.dist(support, support, metric="sqeuclidean")

    per_pair: list[PairResult] = []
    for k in range(n_pairs):
        seed_a = seed_base + k
        seed_b = seed_a + 1000
        rng_a = np.random.default_rng(seed_a)
        rng_b = np.random.default_rng(seed_b)
        state_a = _make_flow_state(rng_a, names, grid_size, dirichlet_concentration)
        state_b = _make_flow_state(rng_b, names, grid_size, dirichlet_concentration)

        def _d(sa: FlowState, sb: FlowState) -> float:
            return _sum_w2_squared(
                sa,
                sb,
                support=support,
                cost_matrix=cost_matrix,
                epsilon=sinkhorn_epsilon,
                n_iter=sinkhorn_iters,
                debiased=True,
            )

        trajectory: list[float] = [_d(state_a, state_b)]
        for _ in range(steps):
            state_a = jko.step(state_a)
            state_b = jko.step(state_b)
            trajectory.append(_d(state_a, state_b))

        lambda_hat, fit_points = _fit_lambda(trajectory, h=h * max(n_inner, 1))
        monotone, rebounded = _trajectory_diagnostics(trajectory)
        per_pair.append(
            PairResult(
                seed_a=seed_a,
                seed_b=seed_b,
                trajectory=trajectory,
                lambda_hat=lambda_hat,
                fit_points=fit_points,
                monotone=monotone,
                rebounded=rebounded,
            )
        )

    classification, lambda_hat_mean, lambda_hat_std = _classify(per_pair)

    plot_path = output_json.with_suffix("").parent / "lambda_monotonicity_plot.png"
    plot_made, plot_note = _try_make_plot(per_pair, plot_path)

    result: dict[str, Any] = {
        "classification": classification,
        "lambda_hat_mean": lambda_hat_mean,
        "lambda_hat_std": lambda_hat_std,
        "n_pairs": n_pairs,
        "steps": steps,
        "seed_base": seed_base,
        "grid_size": grid_size,
        "h": h,
        "n_inner": n_inner,
        "sinkhorn_epsilon": sinkhorn_epsilon,
        "coupling_variant": (
            coupling_variant
            if coupling_variant is not None
            else getattr(species, "coupling_variant", "default")
        ),
        "per_pair": [
            {
                "seed_a": p.seed_a,
                "seed_b": p.seed_b,
                "lambda_hat": p.lambda_hat,
                "fit_points": p.fit_points,
                "monotone_decreasing": p.monotone,
                "rebounded": p.rebounded,
                "d0": p.trajectory[0],
                "d_final": p.trajectory[-1],
            }
            for p in per_pair
        ],
        "trajectories": [p.trajectory for p in per_pair],
        "plot": {
            "path": str(plot_path) if plot_made else None,
            "note": plot_note,
        },
    }

    output_json.parent.mkdir(parents=True, exist_ok=True)
    output_json.write_text(json.dumps(result, indent=2))
    return result


def _parse_args(argv: list[str] | None = None) -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description="Empirical lambda-monotonicity test for the T2 Wasserstein flow."
    )
    parser.add_argument("--steps", type=int, default=100)
    parser.add_argument("--n-pairs", type=int, default=5)
    parser.add_argument("--seed-base", type=int, default=42)
    parser.add_argument(
        "--output-json",
        type=Path,
        default=Path("experiments/lambda_monotonicity_results.json"),
    )
    parser.add_argument(
        "--coupling-variant",
        type=str,
        default=None,
        help="CanonicalSpecies variant to load (dell/levelt). Default: use loader default.",
    )
    parser.add_argument("--grid-size", type=int, default=64)
    parser.add_argument(
        "--h",
        type=float,
        default=0.002,
        help=(
            "JKO step size. Default 0.002 keeps per-step contraction well below "
            "the Sinkhorn-divergence floor so lambda can be fit over many samples."
        ),
    )
    parser.add_argument(
        "--n-inner",
        type=int,
        default=1,
        help="JKO projected-gradient inner substeps per sampled t. Default 1.",
    )
    parser.add_argument(
        "--sinkhorn-epsilon",
        type=float,
        default=0.01,
        help="Entropic regularization epsilon for the Sinkhorn-divergence distance.",
    )
    parser.add_argument("--sinkhorn-iters", type=int, default=500)
    parser.add_argument(
        "--dirichlet-concentration",
        type=float,
        default=1.0,
        help="Dirichlet alpha (same for every bin). 1.0 = uniform on the simplex.",
    )
    return parser.parse_args(argv)


def _print_summary(result: dict[str, Any]) -> None:
    print(f"classification      : {result['classification']}")
    print(f"lambda_hat_mean     : {result['lambda_hat_mean']:.6f}")
    print(f"lambda_hat_std      : {result['lambda_hat_std']:.6f}")
    print(f"n_pairs / steps     : {result['n_pairs']} / {result['steps']}")
    print(f"coupling_variant    : {result['coupling_variant']}")
    print(f"grid_size / h       : {result['grid_size']} / {result['h']}")
    for pair in result["per_pair"]:
        print(
            f"  pair ({pair['seed_a']:>5d}, {pair['seed_b']:>5d}): "
            f"lambda_hat={pair['lambda_hat']:+.5f}  "
            f"fit_pts={pair['fit_points']:>3d}  "
            f"mono={pair['monotone_decreasing']!s:>5s}  "
            f"rebound={pair['rebounded']!s:>5s}  "
            f"d0={pair['d0']:.3e} -> d_final={pair['d_final']:.3e}"
        )
    plot = result["plot"]
    if plot["path"]:
        print(f"plot                : {plot['path']}")
    elif plot["note"]:
        print(f"plot                : SKIPPED ({plot['note']})")


def main(argv: list[str] | None = None) -> int:
    args = _parse_args(argv)
    result = run(
        steps=args.steps,
        n_pairs=args.n_pairs,
        seed_base=args.seed_base,
        output_json=args.output_json,
        coupling_variant=args.coupling_variant,
        grid_size=args.grid_size,
        h=args.h,
        n_inner=args.n_inner,
        sinkhorn_epsilon=args.sinkhorn_epsilon,
        sinkhorn_iters=args.sinkhorn_iters,
        dirichlet_concentration=args.dirichlet_concentration,
    )
    _print_summary(result)
    return 0


if __name__ == "__main__":
    sys.exit(main())
